# ==========================================
# Ruby 型変換メソッド (to_...)
# ==========================================
# 文字列、数値、配列などの「型」を変換する際に使用します。

p (1..5).to_a      # to array: 範囲を配列にする -> [1, 2, 3, 4, 5]
p 100.to_s         # to string: 数値を文字列にする -> "100"
p "10".to_i        # to integer: 文字列を整数にする -> 10
p 3.to_f           # to float: 整数を浮動小数点（小数）にする -> 3.0
p [[:a, 1], [:b, 2]].to_h # to hash: 配列をハッシュにする -> {:a=>1, :b=>2}


# ==========================================
# 文字列 (String) 系
# ==========================================
s = " ruby rails "

p s.strip          # 前後の空白を取り除く -> "ruby rails"
p s.upcase         # 全て大文字にする -> " RUBY RAILS "
p s.downcase       # 全て小文字にする -> " ruby rails "
p s.split(" ")     # 指定した文字で区切って配列にする -> ["ruby", "rails"]
p s.include?("ruby") # 特定の文字が含まれるか判定（真偽値） -> true
p s.gsub("ruby", "java") # 文字列を置換する -> " java rails "


# ==========================================
# 数値 (Numeric) 系
# ==========================================
n = -10

p n.abs            # 絶対値を返す -> 10
p 10 / 3.to_f      # 小数点まで計算したい時は to_f を使う -> 3.3333...
p 1.2.round        # 四捨五入 -> 1
p 1.5.round        # 四捨五入 -> 2
p 1.2.ceil         # 切り上げ -> 2
p 1.8.floor        # 切り捨て -> 1


# ==========================================
# 配列 (Array) 系
# ==========================================
a = [3, 1, 4, 1, 5, nil]

p a.first          # 最初の要素を取得 -> 3
p a.last           # 最後の要素を取得 -> nil
p a.compact        # nil（空の要素）を取り除く -> [3, 1, 4, 1, 5]
p a.uniq           # 重複した値を取り除く -> [3, 1, 4, 5, nil]
p a.shuffle        # 中身をランダムに並び替える
p a.join("-")      # 配列の各要素を繋いで一つの文字列にする -> "3-1-4-1-5-"
p a.push(10)       # 末尾に要素を追加する（a << 10 とも書く）


# ==========================================
# 判定系 (最後に ? がつくメソッド)
# ==========================================
# 返り値は必ず true または false になります。

p [].empty?        # 配列が空っぽか？ -> true
p "text".empty?    # 文字列が空か？ -> false
p nil.nil?         # オブジェクトが nil か？ -> true
p 5.positive?      # 正の数か？ -> true
p 5.negative?      # 負の数か？ -> false
p 5.even?          # 偶数か？ -> false
p 5.odd?           # 奇数か？ -> true


